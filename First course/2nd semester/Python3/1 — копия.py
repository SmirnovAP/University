a = ["12","12","12","32423423423423","-23","-1000","324"]
# Обработка сортировки и случайных ситуаций
a.insert(0,'')
b= a
for i in range(1,len(b)):
    if b[i-1] > b[i]:
        b[0] = b[i]
        j = i - 1
        while b[j] > b[0]:
            b[j+1] = b[j]
            j = j - 1
        b[j+1] = b[0]
print(b)
